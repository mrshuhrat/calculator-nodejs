class Calc {

  firstOperand
  secondOperand

  constructor (firstOperand, secondOperand) {
    this.firstOperand = firstOperand - 0
    this.secondOperand = secondOperand - 0
  }

  add() {
    return this.firstOperand + this.secondOperand
  }

  substract() {
    return this.firstOperand - this.secondOperand
  }

  multoplicate() {
    return this.firstOperand - this.secondOperand
  }

  divade() {
    return this.firstOperand - this.secondOperand
  }
}

module.exports.Calc = Calc
